package com.example.javier.practicaequiposfragment.utils;

public class Juegos {
    int imagen;
    String titulo;

    public Juegos(int imagen, String titulo) {
        this.imagen = imagen;
        this.titulo = titulo;
    }

    public int getImagen() {
        return imagen;
    }

    public String getTitulo() {
        return titulo;
    }
}
