package com.system.develop.t3_ejemplodialogo.utils;

public class Elemento {

    int imagen;
    String nombre;

    public Elemento(int imagen, String nombre) {
        this.imagen = imagen;
        this.nombre = nombre;
    }

    public int getImagen() {
        return imagen;
    }

    public String getNombre() {
        return nombre;
    }
}
