package com.example.rpc_examen_1_2_19.utils;

public class Clases {
    int imagen;
    String nombre;

    public Clases(int imagen, String nombre) {
        this.imagen = imagen;
        this.nombre = nombre;
    }

    public int getImagen() {
        return imagen;
    }

    public String getNombre() {
        return nombre;
    }
}
